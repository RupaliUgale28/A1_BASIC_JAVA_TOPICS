package A_DATA_TYPES;

public class Default_values_of_all_data_types 
{
	boolean a;//false
	char b;//blank space
	int c;//0
	int e[];//null
	float d;//0.0
	int[] g;//null
	
	public static void main(String[] args) 
	{
		
		Default_values_of_all_data_types d=new Default_values_of_all_data_types();
		System.out.println("default of boolean is =" +d.a);
		System.out.println("default of character is =" +d.b);
		System.out.println("default of integer is =" +d.c);
		System.out.println("default of float is =" +d.d);
		System.out.println("default of array 1 is =" +d.e);
	    System.out.println("default of array 2 is =" +d.g);
	    

	}


	
}
