package F2_LOOPING_STATEMENTS;

public class a_for_loop 
{
	public static void main(String[] args) 
	{
		//forward printing
		System.out.print("Increases number gradually as follows = ");
		for(int a=1;a<=10;a++)
		{
			System.out.print(" " +a);
			
		}
		System.out.println();
		
		//backward printing
		System.out.print("Decreases number gradually as follows = ");
		
		for(int a=10;a>=1;a--)
		{
			System.out.print(" " +a);
		}
	}
	

}
